# Pad to 256 chars
_CHAR_COUNT = len(_C64_UPPERCASE_ROM) // 8
_PADDING = bytes(8 * (256 - _CHAR_COUNT))
_FONT_ROM = _C64_UPPERCASE_ROM + _PADDING


def get_font_bitmaps() -> np.ndarray:
    """Returns (256, 8, 8) uint8 array of font bitmaps (1=pixel on, 0=off)."""
    bitmaps = np.zeros((256, 8, 8), dtype=np.uint8)
    for char_idx in range(256):
        for row in range(8):
            byte = _FONT_ROM[char_idx * 8 + row]
            for bit in range(8):
                bitmaps[char_idx, row, 7 - bit] = (byte >> bit) & 1
    return bitmaps


def get_palette_rgb() -> np.ndarray:
    """Returns (16, 3) uint8 array of C64 palette colors."""
    return np.array(C64_PALETTE, dtype=np.uint8)
